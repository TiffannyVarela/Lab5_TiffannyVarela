    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            for (int i = 0; i < usuarios.size(); i++) {
            if (txt_usuario.getText().equals(((Usuario)usuarios.get(i)).getN_usuario())&&jpass_contrasena.getText().equals(((Usuario)usuarios.get(i)).getContrasena())) {
                jmenu_clan.setEnabled(true);
                jmenu_mazo.setEnabled(true);
               jmenu_carta.setEnabled(true);
                jd_ingresar.setVisible(false);
                 txt_usuario.setText("");
                jpass_contrasena.setText("");
            }
        }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(jd_ingresar, "Usuario y contrasena no validos");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

