        // TODO add your handling code here:
        String nombre, apellido, n_usuario, contra;
        int edad;
        Color color = Color.RED;
        Date fecha_nac;
        
        nombre=txt_nombre.getText();
        apellido=txt_apellido.getText();
        n_usuario=txt_nusuario.getText();
        contra=txt_contranueva.getText();
        fecha_nac=date_fnacimiento.getDate();
       
