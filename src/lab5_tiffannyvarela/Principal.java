    private void jt_mazo1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jt_mazo1MouseClicked
        // TODO add your handling code here:
        if (evt.isMetaDown()) {
            //seleccionar un nodo con click derecho
            int row=jt_mazo1.getClosestRowForLocation(evt.getX(), evt.getY());
            jt_mazo1.setSelectionRow(row);
            Object v1 = jt_mazo1.getSelectionPath().getLastPathComponent();
            nodo_seleccionado = (DefaultMutableTreeNode) v1;
            if (nodo_seleccionado.getUserObject() instanceof Carta) {
                carta_seleccionada = (Carta) nodo_seleccionado.getUserObject();
                popup_mostar1.show(evt.getComponent(),getX(),evt.getY());
            }
        }
    }//GEN-LAST:event_jt_mazo1MouseClicked

