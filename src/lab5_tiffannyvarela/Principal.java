        modelo.addElement(x);
        cmb_usuariolider.setModel(modelo);
        cmb_usuariolider1.setModel(modelo);
        System.out.println(edad);
        System.out.println(usuarios);
        txt_nombre.setText("");
        txt_apellido.setText("");
        txt_contranueva.setText("");
        txt_nusuario.setText("");
        date_fnacimiento.setDate(new Date());
        btn_color.setBackground(Color.RED);
        
        
        jd_agregarusuario.setVisible(false);
